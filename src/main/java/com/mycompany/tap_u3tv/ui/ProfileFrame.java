    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        try {
            if (!(DBManager.checkUser(userToDisplay.getId()) == 0)) {
                if (JOptionPane.showConfirmDialog(null, "Are you SURE you want to BAN this user?", "Confirm", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    if (DBManager.deactivateUser(userToDisplay) > 0) {
                        JOptionPane.showMessageDialog(null, "[user disabled]");
                    } else {
                        JOptionPane.showMessageDialog(null, "[there was an error]");
                    }
                }
            } else {
                if (JOptionPane.showConfirmDialog(null, "Are you SURE you want to UNBAN this user?", "Confirm", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    if (DBManager.activeuser(userToDisplay.getId()) > 0) {
                        JOptionPane.showMessageDialog(null, "[user activaded]");
                    } else {
                        JOptionPane.showMessageDialog(null, "[there was an error]");
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

