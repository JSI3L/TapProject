    private void displayUserInfo(int userId, int userLogged) {
        for (User user : users) {
            if (user.getId() == userId) {
                imgUser.setIcon(new ImageIcon(new ImageIcon(userToDisplay.getPfpPath()).getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH)));

                labelUser.setText(user.getName());
                labelEmail.setText(user.getEmail());
                try {
                    labelThemes.setText(String.valueOf(DBManager.countUserPost(user.getId())));
                    labelComments.setText(String.valueOf(DBManager.countUserComments(user.getId())));
                    labelLikes.setText(String.valueOf(DBManager.countUserLikes(user.getId())));
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
        for (User user : users) {
            if (user.getId() == userLogged && user.isIsAdmin()) {
                btnDelete.setEnabled(true);
                btnDelete.setVisible(true);
                if (!userToDisplay.isIsActive()) {
                    btnDelete.setText("REACTIVATE USER");
                    btnDelete.setForeground(Color.decode("#94c98f"));
                    btnDelete.setIcon(new ImageIcon(Paths.get("C:\\Users\\angel\\Pictures\\Therum\\i-up.png").toAbsolutePath().toString()));
                }
                break;
            } else {
                btnDelete.setEnabled(false);
                btnDelete.setVisible(false);
            }
        }
    }
