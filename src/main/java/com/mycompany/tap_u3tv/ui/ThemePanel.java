    private void iconLikesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_iconLikesMouseClicked
        try {
            if (DBManager.countPostLikeFromUser(theme.getId(), logged.getId()) == 0) {
                if (DBManager.createLikeOnPost(theme.getId(), logged.getId()) > 0) {
                    System.out.println("Like created :)");
                    display();
                } else {
                    JOptionPane.showMessageDialog(null, "Oops! An error have been occurred while liking this post :c");
                }
            } else {
                if (DBManager.deleteLikeOnPost(theme.getId(), logged.getId()) > 0) {
                    System.out.println("Like deleted :)");
                    display();
                } else {
                    JOptionPane.showMessageDialog(null, "Oops! An error have been occurred while unliking this post :c");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_iconLikesMouseClicked


