    private void display() {
        Image originalImg = new ImageIcon(user.getPfpPath()).getImage();
        imgUser.setIcon(new ImageIcon(originalImg.getScaledInstance(64, 64, Image.SCALE_SMOOTH)));
        repaint();

        labelUsername.setText(user.getName());
        labelTime.setText(theme.getCreatedAt());
        jTextArea1.setText(theme.getContent());

        try {
            labelLikes.setText(DBManager.countPostLikes(theme.getId()) + " Likes");
            labelComments.setText(DBManager.countPostComments(theme.getId()) + " Comments");
            if (DBManager.countPostLikeFromUser(theme.getId(), logged.getId()) > 0) {
                iconLikes.setIcon(new ImageIcon(Paths.get("C:\\Users\\angel\\Pictures\\Therum\\i-heart-enable.png").toAbsolutePath().toString()));
            } else {
                iconLikes.setIcon(new ImageIcon(Paths.get("C:\\Users\\angel\\Pictures\\Therum\\i-heart.png").toAbsolutePath().toString()));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        if (logged.isIsAdmin()) {
            btnRemove.setEnabled(true);
            btnRemove.setVisible(true);
        } else {
            btnRemove.setEnabled(false);
            btnRemove.setVisible(false);
        }

        btnRemove.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (JOptionPane.showConfirmDialog(null, "Are you SURE you want to delete this? \n(It will delete all likes and comments related too)", "Confirm", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    try {
                        if (DBManager.deletePost(theme.getId()) > 0) {
                            JOptionPane.showMessageDialog(null, "Post deleted successfully");
                            parent.dispose();
                            new ForumFrame(logged.getId()).setVisible(true);
                        } else {
                            JOptionPane.showMessageDialog(null, "Oops! An error have been occurred while deleting this post");
                        }
                    } catch (Exception exception) {
                        exception.printStackTrace();
                    }
                }
            }
        });

        MouseAdapter clickOnComments = new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                parent.dispose();
                new ResponsesFrame(logged.getId(), theme).setVisible(true);
            }
        };
        
        labelComments.addMouseListener(clickOnComments);
        iconComments.addMouseListener(clickOnComments);
    }
